package Client.View.Configs;

public class InfoConfig {

    private int back;
    private int passive1Y;
    private int passive2Y;
    private int passive3Y;
    private int passiveX;
    private int passiveDecX;
    private int size;

    public InfoConfig() {
    }

    public int getPassiveDecX() {
        return passiveDecX;
    }

    public void setPassiveDecX(int passiveDecX) {
        this.passiveDecX = passiveDecX;
    }

    public int getSize() {
        return size;
    }

    public void setSize(int size) {
        this.size = size;
    }

    public int getBack() {
        return back;
    }

    public void setBack(int back) {
        this.back = back;
    }

    public int getPassive1Y() {
        return passive1Y;
    }

    public void setPassive1Y(int passive1Y) {
        this.passive1Y = passive1Y;
    }

    public int getPassive2Y() {
        return passive2Y;
    }

    public void setPassive2Y(int passive2Y) {
        this.passive2Y = passive2Y;
    }

    public int getPassive3Y() {
        return passive3Y;
    }

    public void setPassive3Y(int passive3Y) {
        this.passive3Y = passive3Y;
    }

    public int getPassiveX() {
        return passiveX;
    }

    public void setPassiveX(int passiveX) {
        this.passiveX = passiveX;
    }
}
