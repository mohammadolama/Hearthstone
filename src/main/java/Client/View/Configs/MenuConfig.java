package Client.View.Configs;

public class MenuConfig {
    private int startY;
    private int startX;
    private int spcaing;

    public MenuConfig() {
    }

    public int getStartY() {
        return startY;
    }

    public void setStartY(int startY) {
        this.startY = startY;
    }

    public int getStartX() {
        return startX;
    }

    public void setStartX(int startX) {
        this.startX = startX;
    }

    public int getSpcaing() {
        return spcaing;
    }

    public void setSpcaing(int spcaing) {
        this.spcaing = spcaing;
    }
}
