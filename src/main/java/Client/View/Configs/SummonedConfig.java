package Client.View.Configs;

public class SummonedConfig {

    private int width;
    private int height;

    private int damageX;
    private int hpX;
    private int damageYl;

    public SummonedConfig() {
    }

    public int getWidth() {
        return width;
    }

    public void setWidth(int width) {
        this.width = width;
    }

    public int getHeight() {
        return height;
    }

    public void setHeight(int height) {
        this.height = height;
    }

    public int getDamageX() {
        return damageX;
    }

    public void setDamageX(int damageX) {
        this.damageX = damageX;
    }

    public int getHpX() {
        return hpX;
    }

    public void setHpX(int hpX) {
        this.hpX = hpX;
    }

    public int getDamageYl() {
        return damageYl;
    }

    public void setDamageYl(int damageYl) {
        this.damageYl = damageYl;
    }
}
