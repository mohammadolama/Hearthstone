package Client.View.Configs;

public class LoginConfig {
    private int userlabelX;
    private int userLabelY;
    private int userLabelWidth;
    private int userLabelHeight;

    public LoginConfig() {
    }

    public int getUserlabelX() {
        return userlabelX;
    }

    public void setUserlabelX(int userlabelX) {
        this.userlabelX = userlabelX;
    }

    public int getUserLabelY() {
        return userLabelY;
    }

    public void setUserLabelY(int userLabelY) {
        this.userLabelY = userLabelY;
    }

    public int getUserLabelWidth() {
        return userLabelWidth;
    }

    public void setUserLabelWidth(int userLabelWidth) {
        this.userLabelWidth = userLabelWidth;
    }

    public int getUserLabelHeight() {
        return userLabelHeight;
    }

    public void setUserLabelHeight(int userLabelHeight) {
        this.userLabelHeight = userLabelHeight;
    }
}
