package Client.View.Configs;

public class StatusConfig {
    private int startX;
    private int startY;
    private int width;
    private int height;
    private int backX;
    private int deleteX;
    private int exitX;
    private int backY;
    private int backSize;
    private int infoX;
    private int infoY;
    private int deckX;


    public StatusConfig() {
    }

    public int getStartX() {
        return startX;
    }

    public void setStartX(int startX) {
        this.startX = startX;
    }

    public int getStartY() {
        return startY;
    }

    public void setStartY(int startY) {
        this.startY = startY;
    }

    public int getWidth() {
        return width;
    }

    public void setWidth(int width) {
        this.width = width;
    }

    public int getHeight() {
        return height;
    }

    public void setHeight(int height) {
        this.height = height;
    }

    public int getBackX() {
        return backX;
    }

    public void setBackX(int backX) {
        this.backX = backX;
    }

    public int getDeleteX() {
        return deleteX;
    }

    public void setDeleteX(int deleteX) {
        this.deleteX = deleteX;
    }

    public int getExitX() {
        return exitX;
    }

    public void setExitX(int exitX) {
        this.exitX = exitX;
    }

    public int getBackY() {
        return backY;
    }

    public void setBackY(int backY) {
        this.backY = backY;
    }

    public int getBackSize() {
        return backSize;
    }

    public void setBackSize(int backSize) {
        this.backSize = backSize;
    }

    public int getInfoX() {
        return infoX;
    }

    public void setInfoX(int infoX) {
        this.infoX = infoX;
    }

    public int getInfoY() {
        return infoY;
    }

    public void setInfoY(int infoY) {
        this.infoY = infoY;
    }

    public int getDeckX() {
        return deckX;
    }

    public void setDeckX(int deckX) {
        this.deckX = deckX;
    }
}

