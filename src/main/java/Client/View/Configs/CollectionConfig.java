package Client.View.Configs;

public class CollectionConfig {
    private int searchHeight;
    private int searchX;
    private int searchY;
    private int allCardsX;
    private int allCardWidth;
    private int manaX;
    private int manaY;
    private int manaWidth;

    private int deckX;
    private int deckY;
    private int deckWidth;
    private int deckHeight;
    private int deckSpacing;

    public CollectionConfig() {
    }

    public int getSearchHeight() {
        return searchHeight;
    }

    public void setSearchHeight(int searchHeight) {
        this.searchHeight = searchHeight;
    }

    public int getSearchX() {
        return searchX;
    }

    public void setSearchX(int searchX) {
        this.searchX = searchX;
    }

    public int getSearchY() {
        return searchY;
    }

    public void setSearchY(int searchY) {
        this.searchY = searchY;
    }

    public int getAllCardsX() {
        return allCardsX;
    }

    public void setAllCardsX(int allCardsX) {
        this.allCardsX = allCardsX;
    }

    public int getAllCardWidth() {
        return allCardWidth;
    }

    public void setAllCardWidth(int allCardWidth) {
        this.allCardWidth = allCardWidth;
    }

    public int getManaX() {
        return manaX;
    }

    public void setManaX(int manaX) {
        this.manaX = manaX;
    }

    public int getManaY() {
        return manaY;
    }

    public void setManaY(int manaY) {
        this.manaY = manaY;
    }

    public int getManaWidth() {
        return manaWidth;
    }

    public void setManaWidth(int manaWidth) {
        this.manaWidth = manaWidth;
    }

    public int getDeckX() {
        return deckX;
    }

    public void setDeckX(int deckX) {
        this.deckX = deckX;
    }

    public int getDeckY() {
        return deckY;
    }

    public void setDeckY(int deckY) {
        this.deckY = deckY;
    }

    public int getDeckWidth() {
        return deckWidth;
    }

    public void setDeckWidth(int deckWidth) {
        this.deckWidth = deckWidth;
    }

    public int getDeckHeight() {
        return deckHeight;
    }

    public void setDeckHeight(int deckHeight) {
        this.deckHeight = deckHeight;
    }

    public int getDeckSpacing() {
        return deckSpacing;
    }

    public void setDeckSpacing(int deckSpacing) {
        this.deckSpacing = deckSpacing;
    }
}
