package Client.View.Configs;

public class CollectionDrawingConfig {

    private int searchHeight;
    private int searchY;
    private int manaY;
    private int cardsX;
    private int cardsY;
    private int cardHeight;
    private int cardWidth;

    public CollectionDrawingConfig() {
    }

    public int getSearchHeight() {
        return searchHeight;
    }

    public void setSearchHeight(int searchHeight) {
        this.searchHeight = searchHeight;
    }

    public int getSearchY() {
        return searchY;
    }

    public void setSearchY(int searchY) {
        this.searchY = searchY;
    }

    public int getManaY() {
        return manaY;
    }

    public void setManaY(int manaY) {
        this.manaY = manaY;
    }

    public int getCardsX() {
        return cardsX;
    }

    public void setCardsX(int cardsX) {
        this.cardsX = cardsX;
    }

    public int getCardsY() {
        return cardsY;
    }

    public void setCardsY(int cardsY) {
        this.cardsY = cardsY;
    }

    public int getCardHeight() {
        return cardHeight;
    }

    public void setCardHeight(int cardHeight) {
        this.cardHeight = cardHeight;
    }

    public int getCardWidth() {
        return cardWidth;
    }

    public void setCardWidth(int cardWidth) {
        this.cardWidth = cardWidth;
    }
}
